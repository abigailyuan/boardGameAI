
public class qizi {

	private int x;
	private int y;
	
	public qizi(int i, int row) {
		this.setX(i);
		this.setY(row);
	}

	private void setY(int row) {
		this.y = row;
		
	}

	public int getX() {
		return this.x;
	}

	public void setX(int i) {
		this.x = i;
	}

	public int getY() {
		
		return this.y;
	}
}
